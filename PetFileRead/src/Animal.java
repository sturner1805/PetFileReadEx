
public class Animal {
	
	private String name;
	private String species;
	
	Animal(String name, String species){
		this.name=name;
		this.species=species;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getspecies() {
		return species;
	}

	public void setspecies(String species) {
		this.species = species;
	}

}
